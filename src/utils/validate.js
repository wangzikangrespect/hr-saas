/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validatemobile(str) {
  return /^1[3-9]\d{9}$/.test(str) // 手机格式验证规则1
}
